#Menampilkan informasi luas persegi panjang
print("Menghitung Luas Persegi Panjang")

#Input nilai panjang dan lebar
p = float(input("Masukkan nilai panjang:"))
l = float(input("Masukkan nilai lebar:"))

#Menghitung Luas Persegi Panjang
luas_persegipanjang = p * l

#Menampilkan Hasil Perhitungan
print("Luas Persegi Panjang adalah", luas_persegipanjang)

#Menampilkan informasi luas lingkaran
print("Menghitung Luas Lingkaran")

#Input nilai jari-jari
r = float(input("Masukkan nilai jar-jari:"))

#Menghitung Luas Lingkaran
luas_lingkaran = 3.14 * (r ** 2)

#Menampilkan Hasil Luas Lingkaran
print("Luas Lingkaran adalah", luas_lingkaran)

#Menampilkan informasi luas sisi kubus
print("Menghitung Luas Sisi Kubus")

#Input nilai sisi
s = float(input("Masukkan nilai sisi:"))

#Menghitung Luas Sisi Kubus
luas_kubus = 6 * (s ** 2)

#Menampilkan Hasil Luas Sisi Kubus
print("Luas Sisi Kubus:", luas_kubus)

#Menampilkan informasi konversi suhu celcius ke fahrenheit
print("Konversi Suhu Celcius ke Fahrenheit")

#Input nilai celcius
C = float(input("Masukkan nilai celcius:"))

#Menghitung konversi suhu celcius ke fahrenheit
F = (9/5) * C + 32

#Menampilkan hasil konversi suhu celcius ke fahrenheit
print("Fahrenheit:", F)

#Menampilkan informasi konversi suhu reamur ke kelvin
print("Konversi Suhu Reamur ke Kelvin")

#Input nilai reamur
R = float(input("Masukkan nilai reamur:"))

#Menghitung konversi suhu reamur ke kelvin
K = (5/4) * R + 273

#Menampilkan hasil konversi suhu reamur ke kelvin
print("Kelvin:", K)